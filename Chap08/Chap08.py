
# List are Mutable

lotto = [2, 14, 35, 34, 25]
print(lotto)

lotto[2] = 13   # 2번 방에 있는 원소를 바꿈
print(lotto)

# How Long is a List?

greet = 'Hello Bob!'
print(len(greet))

x = [1, 2, 'joe', 66]
print(len(x))

# Using the range function

friends = ['Joseph', 'Lee', 'Sally', 'Jenny']
print(len(friends))
print(range(len(friends)))

